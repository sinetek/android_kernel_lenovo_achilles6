static void lcm_init(void)
{

	unsigned char cmd = 0x0;
	unsigned char data = 0xFF;
	int ret = 0;
	cmd = 0x00;
	data = 0x14;

	LCM_LOGD("lcm_init\n");

	OCP2131_GPIO_ENP_enable();
	ret = OCP2131_write_bytes(cmd, data);
	if (ret < 0)
		LCM_LOGI("hx83102e----ocp2131----cmd=%0x--i2c write error----\n", cmd);
	else
		LCM_LOGI("hx83102e----ocp2131----cmd=%0x--i2c write success----\n", cmd);

	MDELAY(2);
	OCP2131_GPIO_ENN_enable();
	cmd = 0x01;
	data = 0x14;

	ret = OCP2131_write_bytes(cmd, data);

	if (ret < 0)
		LCM_LOGI("hx83102e----ocp2131----cmd=%0x--i2c write error----\n", cmd);
	else
		LCM_LOGI("hx83102e----ocp2131----cmd=%0x--i2c write success----\n", cmd);

	MDELAY(5);
	SET_RESET_PIN(1);
	MDELAY(1);
	SET_RESET_PIN(0);
	tpd_gpio_output(0, 1);
	MDELAY(1);
	tpd_gpio_output(0, 0);
	MDELAY(5);
	tpd_gpio_output(0, 1);
	SET_RESET_PIN(1);
	MDELAY(150);

	// init_lcm_register();
	push_table(NULL, init_setting_vdo, sizeof(init_setting_vdo) / sizeof(struct LCM_setting_table), 1);
	LCM_LOGI("hx83102e----BOE----lcm mode = vdo mode ----\n");

	BACKLIGHT_GPIO_enable();
	SGM37604A_set_backlight_reg_init();
}

